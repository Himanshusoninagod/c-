#include<iostream>
using namespace std;
int main(){
    
    //switch case -

    int week;
    cout<<"Enter week name :";
    cin>>week;

    switch(week){
        case 1:
        cout<<"Monday";
        break;

        case 2:
        cout<<"Tuesday";
        break;

        case 3:
        cout<<"Wednesday";
        break;

        case 4:
        cout<<"Thursday";
        break;

        case 5:
        cout<<"Friday";
        break;

        case 6:
        cout<<"Saturdayday";
        break;

        case 7:
        cout<<"Sunday";
        break;

        default:
        cout<<"invalid";

    }




}